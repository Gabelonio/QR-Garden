package clasesCultivo;
/*
 * This class contains the information 
 * of an area, including its setters and getters.
 */
public class Malla {

	/*
	 * Attributes
	 */
	String ID,tipo;
	
	/*
	 * Setters and Getters
	 */
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	} 
}
